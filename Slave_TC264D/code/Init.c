/*********************************************************************************************************************
* 函数名称          total_init
* 功能说明          下位机总初始化函数
* 参数说明          无
* 返回参数          无
* 使用示例          total_init();
* 备注信息          当前阶段依次接入电机 舵机 状态 控制 与通信模块初始化
*                  编码器板级接线已固定为 P20_3 与 P20_0 对应 TIM6_ENCODER
*                  舵机板级推荐 IO 已固定为 P33_9
*                  当前通信模块采用轮询串口方式 后续可继续扩展为中断方式
*********************************************************************************************************************/
void total_init (void)
{
    motor_init();
    servo_init();
    state_init();
    control_init();
    communication_init();
}
