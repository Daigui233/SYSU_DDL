#include "Servo.h"

servo_info_struct servo_info = {SERVO_DUTY_MID};

/*********************************************************************************************************************
* 函数名称          servo_duty_limit
* 功能说明          对舵机占空比进行限幅
* 参数说明          duty            输入舵机占空比
* 返回参数          uint32          限幅后的舵机占空比
* 使用示例          servo_duty_limit(1000);
* 备注信息          本函数为 Servo 模块内部静态函数
*                  当前先采用固定占空比范围保护舵机机械限位
*                  后续若实车安装位置变化 需重新标定 SERVO_DUTY_MAX SERVO_DUTY_MID SERVO_DUTY_MIN
*********************************************************************************************************************/
static uint32 servo_duty_limit (uint32 duty)
{
    if(SERVO_DUTY_MAX < duty)
    {
        duty = SERVO_DUTY_MAX;
    }
    else if(SERVO_DUTY_MIN > duty)
    {
        duty = SERVO_DUTY_MIN;
    }

    return duty;
}

/*********************************************************************************************************************
* 函数名称          servo_init
* 功能说明          初始化舵机 PWM 输出
* 参数说明          无
* 返回参数          无
* 使用示例          servo_init();
* 备注信息          当前板级推荐 IO 为 P33_9 因此本模块默认使用 ATOM0_CH7_P33_9
*                  若后续板级重新分配 IO 仅需修改 Servo.h 中的 SERVO_PWM_PIN 宏定义
*********************************************************************************************************************/
void servo_init (void)
{
    pwm_init(SERVO_PWM_PIN, SERVO_PWM_FREQ, SERVO_DUTY_MID);

    servo_info.duty = SERVO_DUTY_MID;
}

/*********************************************************************************************************************
* 函数名称          servo_set_duty
* 功能说明          设置舵机占空比输出
* 参数说明          duty            舵机目标占空比
* 返回参数          无
* 使用示例          servo_set_duty(SERVO_DUTY_MID);
* 备注信息          当前阶段仅提供底层直接输出接口
*                  不在本模块内提前加入转角换算或转向闭环逻辑 以便后续单独搭建 PID 与 Control 模块
*********************************************************************************************************************/
void servo_set_duty (uint32 duty)
{
    duty = servo_duty_limit(duty);
    servo_info.duty = duty;

    pwm_set_duty(SERVO_PWM_PIN, duty);
}

/*********************************************************************************************************************
* 函数名称          servo_center
* 功能说明          舵机回中
* 参数说明          无
* 返回参数          无
* 使用示例          servo_center();
* 备注信息          本函数等效于 servo_set_duty(SERVO_DUTY_MID)
*********************************************************************************************************************/
void servo_center (void)
{
    servo_set_duty(SERVO_DUTY_MID);
}
