#include "State.h"

state_info_struct state_info =
{
    STATE_IDLE,
    STATE_IDLE,
    0
};

/*********************************************************************************************************************
* 函数名称          state_init
* 功能说明          状态模块初始化函数
* 参数说明          无
* 返回参数          无
* 使用示例          state_init();
* 备注信息          当前阶段默认上电进入 STATE_IDLE
*********************************************************************************************************************/
void state_init (void)
{
    state_info.current_state = STATE_IDLE;
    state_info.last_state = STATE_IDLE;
    state_info.state_changed = 0;
}

/*********************************************************************************************************************
* 函数名称          state_set
* 功能说明          设置当前状态
* 参数说明          state           目标状态
* 返回参数          无
* 使用示例          state_set(STATE_TRACK);
* 备注信息          当前主要用于后续承接上位机下发状态与本地下位机保护状态
*********************************************************************************************************************/
void state_set (car_state_enum state)
{
    if(state >= STATE_MAX)
    {
        return;
    }

    if(state_info.current_state != state)
    {
        state_info.last_state = state_info.current_state;
        state_info.current_state = state;
        state_info.state_changed = 1;
    }
}

/*********************************************************************************************************************
* 函数名称          state_get
* 功能说明          获取当前状态
* 参数说明          无
* 返回参数          car_state_enum  当前状态
* 使用示例          current = state_get();
* 备注信息          控制模块后续通过本接口读取当前状态
*********************************************************************************************************************/
car_state_enum state_get (void)
{
    return state_info.current_state;
}

/*********************************************************************************************************************
* 函数名称          state_is_changed
* 功能说明          查询状态是否发生变化
* 参数说明          无
* 返回参数          uint8           1 表示变化 0 表示未变化
* 使用示例          if(state_is_changed())
* 备注信息          便于控制模块在状态切换时重载参数与复位 PID
*********************************************************************************************************************/
uint8 state_is_changed (void)
{
    return state_info.state_changed;
}

/*********************************************************************************************************************
* 函数名称          state_clear_changed
* 功能说明          清除状态变化标志
* 参数说明          无
* 返回参数          无
* 使用示例          state_clear_changed();
* 备注信息          控制模块处理完状态切换后可调用本接口
*********************************************************************************************************************/
void state_clear_changed (void)
{
    state_info.state_changed = 0;
}
